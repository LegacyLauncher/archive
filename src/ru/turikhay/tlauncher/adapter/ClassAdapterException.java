package ru.turikhay.tlauncher.adapter;

public class ClassAdapterException extends Exception {
	private static final long serialVersionUID = -2267109893874281890L;
	
	ClassAdapterException() {
		super();
	}
	
	ClassAdapterException(String message, Throwable cause) {
		super(message, cause);
	}
	
	ClassAdapterException(Throwable cause) {
		super(cause);
	}

}
